import turtle

#绘制五角星
turtle.setup(1000, 1000)
turtle.pensize(5)
turtle.penup()
turtle.goto(0, 300)
turtle.setheading(-72)
turtle.pendown()
turtle.forward(600)
turtle.right(144)
turtle.forward(600)
turtle.right(144)
turtle.forward(600)
turtle.right(144)
turtle.forward(600)
turtle.right(144)
turtle.forward(600)
turtle.turtlesize(3)
turtle.done()