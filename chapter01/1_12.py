import turtle

#绘制正方形
turtle.setup(1000, 1000)
turtle.pensize(5)
turtle.forward(300)
turtle.left(90)
turtle.forward(300)
turtle.left(90)
turtle.forward(600)
turtle.left(90)
turtle.forward(600)
turtle.left(90)
turtle.forward(600)
turtle.left(90)
turtle.forward(300)
turtle.penup()
turtle.goto(0, 0)
turtle.pendown()
turtle.left(90)
turtle.forward(300)
turtle.penup()
turtle.goto(0, -300)
turtle.pendown()
turtle.right(90)
turtle.forward(600)
turtle.penup()
turtle.goto(0, 0)
turtle.setheading(-90)
turtle.pendown()
turtle.turtlesize(3)
turtle.done()
