# -*- coding: utf-8 -*-

import turtle

turtle.setup(1000, 1000)
t1 = turtle.Turtle()
t1.pensize(5)
t1.penup()
t1.goto(0, -300)
t1.pendown()
t1.circle(300)
t1.penup()
t1.goto(-100, -60)
t1.pendown()
t1.seth(60)
t1.forward(200)
t1.right(120)
t1.forward(200)
t1.right(120)
t1.forward(200)
t1.penup()
t1.goto(-200, -60)
t1.pendown()
t1.goto(0, -200)
t1.goto(200, -60)
t1.penup()
t1.goto(-150, 150)
t1.pendown()
t1.begin_fill()
t1.circle(40)
t1.penup()
t1.goto(150, 150)
t1.pendown()
t1.circle(40)
t1.end_fill()
t1.hideturtle()
turtle.done()