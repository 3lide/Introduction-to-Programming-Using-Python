import turtle

turtle.setup(1000, 1000)
t1 = turtle.Turtle()
t1.color("red")
t1.fillcolor("red")
t1.penup()
t1.goto(-300, 0)
t1.seth(60)
t1.pendown()
t1.begin_fill()
t1.forward(300)
t1.right(60)
t1.forward(300)
t1.right(60)
t1.forward(300)
t1.right(60)
t1.forward(300)
t1.right(60)
t1.forward(300)
t1.right(60)
t1.forward(300)
t1.end_fill()
t1.color("white")
t1.penup()
t1.goto(0, -100)
t1.pendown()
t1.write("STOP",align="center",font=("Times New Roman", 120, "bold"))
t1.hideturtle()
turtle.done()